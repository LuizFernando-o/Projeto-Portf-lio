                <br>
                <p>
                    Heluka é um jogo de plataforma 2D criado a partir do Unity2D. O jogo ensina de forma divertida o que cada área
                    da Ciência da Computação faz. Nele, a personagem principal está indecisa sobre qual carreira seguir
                    após se formar e acaba caindo em um sono profundo e começa a sonhar com estas áreas. Cada estágio ensina
                    sobre as qualidades que um profissional deve ter para atuar na área e o que ele irá fazer se decidir seguir tal carreira.
                </p>
                <hr>
                <p>Membro da equipe de desenvolvimento com foco no código de programação e na criação das músicas temas.</p>

